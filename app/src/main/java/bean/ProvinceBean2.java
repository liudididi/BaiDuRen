public class ProvinceBean2 {

    private String score;
    private String time;
    private String year;

    public ProvinceBean2(String score, String time, String year) {
        this.score = score;
        this.time = time;
        this.year = year;
    }

    public String getScore() {
        return score;
    }

    public String getTime() {
        return time;
    }

    public String getYear() {
        return year;
    }
}
